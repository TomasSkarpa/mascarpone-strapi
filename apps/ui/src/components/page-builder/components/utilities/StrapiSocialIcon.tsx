export function StrapiSocialIcon({ component }: StrapiSocialIconProps) {
  if (!component?.url || !component?.platform) {
    return null
  }

  const icon = component.customIcon ? (
    <div dangerouslySetInnerHTML={{ __html: component.customIcon }} />
  ) : (
    platformIcons[component.platform as keyof typeof platformIcons]
  )

  if (!icon) return null

  const displayName =
    component.name ||
    component.platform.charAt(0).toUpperCase() + component.platform.slice(1)

  return (
    <a
      href={component.url}
      target="_blank"
      rel="noopener noreferrer"
      className="transition-opacity duration-500 hover:opacity-70"
      title={`${displayName} - Opens in a new tab`}
      aria-label={displayName}
    >
      <figure>{icon}</figure>
    </a>
  )
}

export default StrapiSocialIcon
